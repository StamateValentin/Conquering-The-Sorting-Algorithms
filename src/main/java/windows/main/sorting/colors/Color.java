package windows.main.sorting.colors;

public class Color {
    public final int r;
    public final int g;
    public final int b;

    public Color(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public Color(int c) {
        this.r = this.g = this.b = c;
    }

}
